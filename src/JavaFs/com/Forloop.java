package JavaFs.com;

public class Forloop {

	public static void main(String[] args) {
		
		//Print the numbers from 1 to 10//
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		//Even number //
		for(int i=2;i<=10;i=i+2)
		{
			System.out.println(i);
		}
        
        //Odd number //
        for(int i=1;i<=10;i=i+2)
        {
            System.out.println(i);
            
        }    
        
        System.out.println("mul");
        
        for(int i=2;i<10;i=i*2)
        {
            System.out.println(i);
            
        } 
        

      
        
        
        
        
        
        
        
        
        
        
        
	}

}
