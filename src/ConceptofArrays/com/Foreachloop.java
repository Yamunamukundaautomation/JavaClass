package ConceptofArrays.com;

public class Foreachloop {

	public static void main(String[] args) {
		float f[]= {7.5f,8.1f,9.7f,4.8f,5.6f,6.5f};
		for(float g:f)
			{
				System.out.println(g);
			}
			
		long l[]= {7L,8L,9L,4L,5L,6L};
		for(long m:l)
			{
				System.out.println(m);
			}
		}

	}


